private void tampilkanLaporan() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Layanan");
        model.addColumn("NIK");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Tanggal Lahir");
        model.addColumn("Status Validasi");
        model.addColumn("Waktu Pengajuan");

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/simapel", "root", "");
            String sql = "SELECT * FROM permohonan WHERE status_validasi_admin = 'selesai'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_permohonan"),
                    rs.getString("layanan"),
                    rs.getString("nik"),
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getString("ttl"),
                    rs.getString("status_validasi_admin"),
                    rs.getString("waktu_pengajuan")
                });
            }

            jTable1.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan laporan: " + e.getMessage());
        }
    }

