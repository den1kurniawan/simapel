private void getData() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Hapus semua baris di tabel

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/simapel", "root", "");
        String sql = "SELECT * FROM permohonan WHERE status_validasi_admin = 'selesai'";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            String[] data = {
                rs.getString("id_permohonan"),
                rs.getString("layanan"),
                rs.getString("nik"),
                rs.getString("nama"),
                rs.getString("alamat"),
                rs.getString("ttl"),
                rs.getString("status_validasi_admin"),
                rs.getString("waktu_pengajuan")
            };
            model.addRow(data);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data laporan: " + e.getMessage());
    }
}
