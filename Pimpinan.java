private void tampilkanPengaduan() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Isi Pengaduan");
    model.addColumn("Waktu Pengaduan");

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/simapel", "root", "");
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM pengaduan ORDER BY waktu_pengaduan DESC");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_pengaduan"),
                rs.getString("isi_pengaduan"),
                rs.getTimestamp("waktu_pengaduan")
            });
        }

        tblPengaduan.setModel(model);
    } catch (Exception e) {
        e.printStackTrace();
    }
}
private void tampilkanPermohonanValid() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Layanan");
    model.addColumn("NIK");
    model.addColumn("Nama");
    model.addColumn("Waktu");
    model.addColumn("Status");

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/simapel", "root", "");
        String sql = "SELECT * FROM permohonan WHERE status_validasi_admin = 'on proses'";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_permohonan"),
                rs.getString("layanan"),
                rs.getString("nik"),
                rs.getString("nama"),
                rs.getString("waktu_pengajuan"),
                rs.getString("status_validasi_admin")
            });
        }

        tblPermohonan.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal ambil data: " + e.getMessage());
    }
}

