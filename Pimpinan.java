    private void btnACCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnACCActionPerformed
btnACC.addActionListener(e -> {
    int row = tblPermohonan.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Pilih data yang akan di-TTD!");
        return;
    }

    String id = tblPermohonan.getValueAt(row, 0).toString();

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/simapel", "root", "");
        String sql = "UPDATE permohonan SET status_validasi_admin = 'selesai' WHERE id_permohonan = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, id);
        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Permohonan berhasil di-TTD oleh pimpinan.");
        tampilkanPermohonanValid(); // refresh tabel
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Gagal TTD: " + ex.getMessage());
    }
});
    }//GEN-LAST:event_btnACCActionPerformed

